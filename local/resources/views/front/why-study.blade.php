                                    <li>Extra-curricular activities</li>
                                </ul>
                                <div class="content-txt txt-indent">
                                    <p>Another aspect parents should look at when selecting a school is the opportunity students get to take their learning out of the classroom. Study trips, extra-curricular activities and opportunities for community work not only provide students with a way to explore their interests, apply skills and concepts from class in real-life settings, and to become well- rounded people, these activities also help students with their university applications. With more students now going to college year after year, participation in extra-curricular activities demonstrates that students are well-rounded and have more to offer than academic prowess. Schools that offer a more process oriented approach to extra-curriculars will put an emphasis on creating ways for students to grow intellectually, emotionally and in their ability to lead and take on serious responsibility-all of which will highlight their core strengths.</p>
