                                </ul>
                                <div class="content-txt txt-indent">
                                    <p>Each system has its advantages and disadvantages and of course, each school has its own strengths and weaknesses within their particular curriculum. There are also other curricula which do not fall into any of these categories, but may be developed by an individual school, or by individual people.</p>
